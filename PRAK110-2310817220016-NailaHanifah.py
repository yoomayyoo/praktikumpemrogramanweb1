alas = 5
tinggi = 12
sisi_a = alas
sisi_b = tinggi
sisi_c = (sisi_a**2 + sisi_b**2)**0.5
keliling = sisi_a + sisi_b + sisi_c
luas = 0.5 * alas * tinggi
print("Diketahui :")
print(f"Alas = {alas} cm")
print(f"Tinggi = {tinggi} cm")
print("Jawab :")
print(f"Sisi A = {sisi_a} cm")
print(f"Sisi B = {sisi_b} cm")
print(f"Sisi C = {sisi_c:.2f} cm")
print(f"Keliling = {keliling:.2f} cm")
print(f"Luas = {luas:.2f} cm")